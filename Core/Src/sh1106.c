void SH1106_DrawString(uint8_t x, uint8_t y, const char *str)
{
    uint8_t pixelCol = x * SH1106_CHAR_W;
    uint8_t page     = y & 0x07;
    uint8_t buf[7];
    buf[0] = 0x40;  /* data control byte */
    buf[6] = 0x00;  /* 1-pixel spacing column */

    while (*str && pixelCol < SH1106_WIDTH)
    {
        uint8_t c = (uint8_t)*str++;
        if (c < 32 || c > 127) c = 32;
        SetCursorQueued(page, pixelCol);
        memcpy(&buf[1], font5x8[c - 32], 5);
        Enqueue(buf, 7);
        pixelCol += SH1106_CHAR_W;
    }
}


